# Пример: работа с переменными

# Создадим переменные
имя = "Анна"  # строка (текст)
возраст = 25  # целое число
рост = 1.68  # число с плавающей точкой
геймер = True  # логическое значение (True или False)

# Выведем информацию на экран
print("Имя:", имя)
print("Возраст:", возраст)
print("Рост:", рост)
print("Геймер?", геймер)

# Изменим значение переменной
возраст = возраст + 1
print("Обновленный возраст:", возраст)